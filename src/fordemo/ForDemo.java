package fordemo;

public class ForDemo {


    public static void main(String[] args){


        int array[] = {3,4,1,5};

        for (int index=0;index<array.length; index++){
            System.out.println(array[index]);
        }



        for (int count = 1; count <= 100; count++){
            System.out.println("count "+count);


        }
    }
}
